void ImGuiLayer::OnAttach()
{
    IMGUI_CHECKVERSION();
    ImGui::CreateContext();
    ImGui::StyleColorsDark();

    SetupImGuiStyle();

    // Enable Docking
    ImGui::GetIO().ConfigFlags |= ImGuiConfigFlags_DockingEnable;

    auto &io = ImGui::GetIO();

    // ImFont *roboto = io.Fonts->AddFontFromFileTTF("vendor/imgui/misc/fonts/Roboto-Medium.ttf", 16);
    // io.FontDefault = roboto;

    ImGui_ImplGlfw_InitForOpenGL(static_cast<GLFWwindow *>(opfor::Application::Get().GetWindow()->GetRawHandle()),
                                 true);
    ImGui_ImplOpenGL3_Init("#version 450");
}

void ImGuiLayer::OnDetach()
{
}

void ImGuiLayer::OnImGuiRender()
{
    static bool show = true;

    BeginFrame();
    BeginDockspace();
    ImGui::ShowDemoWindow(&show);
    // WARNING: Do not used range-based for loop here because if we create a window inside a "OnDrawGUI" call
    // the iterators would get invalidated and will probably crash the program... or worse... will corrupt
    // the program in an annoying way.
    for (size_t i = 0; i < _windows.size(); i++)
    {
        _windows[i]->OnDrawGUI();
    }
    EndDockspace();
    EndFrame();

    auto mouse = opfor::Input::GetMouseButton(opfor::MouseButton::ButtonRight) == opfor::KeyStatus::Pressed;
    if (mouse)
    {
        opfor::Application::Get().GetWindow()->HideCursor(true);
    }
    else
    {
        opfor::Application::Get().GetWindow()->HideCursor(false);
    }
}
